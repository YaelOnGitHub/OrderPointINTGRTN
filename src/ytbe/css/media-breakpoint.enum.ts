/**
 * Possible media breakpoints
 * */
export enum MediaBreakpoint {
  /**
   * Small screens - Ex: Smartphones
   * */
  small = "small",
  /**
   * Medium screens - Ex: Tablets
   * */
  medium = "medium",
  /**
   * Large screens - Ex: Large Tablets and Desktops
   * */
  large = "large",
  /**
   * None - item does not break
   * */
  none = "none"
};
